pub fn opacity(col: Color, a_value: f32) Color {
    return Color{ .r = col.r, .g = col.g, .b = col.b, .a = a_value };
}

pub fn brighten(col: Color, bright_input: f32) Color {
    var bright: f32 = bright_input;
    bright = @min(bright, 1.0);
    bright = @max(bright, -1.0);

    if (bright > 0) {
        // bright > 0 gives brighter color by interpolating with white
        const r: f32 = (1 - bright) * col.r + bright;
        const g: f32 = (1 - bright) * col.g + bright;
        const b: f32 = (1 - bright) * col.b + bright;
        const a: f32 = col.a;

        return Color{ .r = r, .g = g, .b = b, .a = a };
    } else {

        // bright < 0 gives darker color by interpolating with black
        const r: f32 = (1 + bright) * col.r;
        const g: f32 = (1 + bright) * col.g;
        const b: f32 = (1 + bright) * col.b;
        const a: f32 = col.a;

        return Color{ .r = r, .g = g, .b = b, .a = a };
    }
}
